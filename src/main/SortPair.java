package main;

public class SortPair {
	
	private int [] output;
	private double [] sorting;
	
	public SortPair (int [] output, double [] sorting) {
		this.output = output;
		this.sorting = sorting;
	}

	public int[] getOutput() {
		return output;
	}

	public double[] getSorting() {
		return sorting;
	}
	
	

}
